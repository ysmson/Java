package tw.shawn.tutor;

public class Shawn03 {

	// 主程式：運算子示範
	public static void main(String[] args) {
		// 宣告 byte 型態的變數 var1 與 var2
		byte var1 = 10;
		byte var2 = 5;

		// 宣告 int 型態的變數 var3
		int var3;

		// 將 var1 與 var2 相加的結果指派給 var3
		var3 = var1 + var2;

		// 輸出 var3 的值（結果為 15）
		System.out.println(var3);
	}
}

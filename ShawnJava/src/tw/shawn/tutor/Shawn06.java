package tw.shawn.tutor;

public class Shawn06 {

	// 字元範例
	public static void main(String[] args) {
		// 使用 Unicode 編碼表示字元
		char c1 = 65;  // 65 代表 'A'，在 Unicode 中 'A' 的編碼為 65
		char c2 = 'D'; // 使用字符直接賦值，表示字母 D
		char c3 = '資'; // 使用 Unicode 來表示中文字 '資'

		// 輸出字符 '資'
		System.out.println(c3);  // 會顯示 "資"
	}
}

package tw.shawn.tutor;

public class Shawn22 {
    public static void main(String[] args) {
        // 定義字串 s1，並將其設為 "Shawn"
        String s1 = "Shawn";
        
        // 使用 charAt(0) 取得字串中索引位置為 0 的字符（'S'）
        System.out.println(s1.charAt(0));
        
        // 直接在字串 "Shawn" 上使用 charAt(1) 取得字串中索引位置為 1 的字符（'h'）
        System.out.println("Shawn".charAt(1));
    }
}
